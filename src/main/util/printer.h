/**
 * Prints the given message to the console.
 *
 * @param m the message
 */
void print(std::string m)
{
    std::cout << m;
}

/**
 * Prints a line break to the console.
 */
void println()
{
    std::cout << std::endl;
}

/**
 * Prints a line break with the given message to the console.
 *
 * @param m the message
 */
void println(std::string m)
{
    std::cout << m << std::endl;
}
